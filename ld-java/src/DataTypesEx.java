public class DataTypesEx {
    public static void main(String[] args) {

        byte byteVar = 126;
        short shortVar = 32000;
        int intVar = 217467647;
        long longVar = 92999992764647687L;
        float floatVar = 13546.6635F;
        double doubleVar = 63576.7537554357D;
        boolean booleanVar = false;
        char charVar = 'V';

        System.out.println("Value of byte: " + byteVar);
        System.out.println("Value of short: " + shortVar);
        System.out.println("Value of int: " + intVar);
        System.out.println("Value of long: " + longVar);
        System.out.println("Value of float: " + floatVar);
        System.out.println("Value of double: " + doubleVar);
        System.out.println("Value of boolean: " + booleanVar);
        System.out.println("Value of char: " + charVar);


    }
}
